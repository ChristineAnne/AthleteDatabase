    private void idTextFieldKeyReleased(java.awt.event.KeyEvent evt) {//GEN-FIRST:event_idTextFieldKeyReleased
        if(evt.getKeyCode() == KeyEvent.VK_ENTER){
            try{
                int data = Integer.parseInt(idTextField.getText());
                if(data < 0){
                    JOptionPane.showMessageDialog(this, "Please use positive numbers for the ID number", "Error", JOptionPane.ERROR_MESSAGE);
                    idTextField.setText("");
                }else{
                    Connection conn = null;
                    Statement stmt = null;
                    try{
                        Class.forName("org.sqlite.JDBC");
                        conn = DriverManager.getConnection(url);
                        stmt = conn.createStatement();

                        String exe = "SELECT athleteID FROM Athlete";
                        ResultSet rs = stmt.executeQuery(exe);
                        
                        Boolean found = false;
                        while(rs.next()){
                            if(rs.getInt("athleteID") == data){
                                found = true;
                                break;
                            }
                        }
                        if(found){
                            id = data;
                            idStatus = true;
                            idTextField.setFocusable(false);
                        }else{
                            JOptionPane.showMessageDialog(this, "Athlete not found. Enter ID number again.", "Error", JOptionPane.ERROR_MESSAGE);
                            idTextField.setText("");
                        }
                    }catch(SQLException e){
                        System.out.println("SQLException from 'RemoveAthlete.java-idTextFieldKeyReleased': " + e.getMessage());
                    }catch(Exception e){
                        System.out.println("Exception from 'RemoveAthlete.java-idTextFieldKeyReleased': " + e.getMessage());
                    }finally{
                        try{
                            if(stmt != null){
                                conn.close();
                            }
                        }catch(SQLException e){
                            e.printStackTrace();
                        }
                    }
                }
            }catch(NumberFormatException e){
                JOptionPane.showMessageDialog(this, "Input must be numeric", "Error", JOptionPane.ERROR_MESSAGE);
                idTextField.setText("");
            }
        }
    }//GEN-LAST:event_idTextFieldKeyReleased

