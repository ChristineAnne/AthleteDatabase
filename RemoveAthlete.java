    private void updateTextArea(){
        Connection conn = null;
        Statement stmt = null;
        ArrayList<Athlete> athleteList = new ArrayList<>();
        
        try{
            Class.forName("org.sqlite.JDBC");
            conn = DriverManager.getConnection(url);
            stmt = conn.createStatement();

            String exe = "SELECT * FROM Athlete";
            ResultSet rs = stmt.executeQuery(exe);
            
            while(rs.next()){
                Athlete athlete = new Athlete(rs.getInt("athleteID"), rs.getString("athleteName"), rs.getInt("athleteAge"), rs.getFloat("athleteHeight"), rs.getFloat("athleteWeight"));
                athleteList.add(athlete);
            }
            
            rs.close();
        }catch(SQLException e){
            System.out.println("SQLException from 'RemoveAthlete.java-updateTextArea': " + e.getMessage());
        }catch(Exception e){
            System.out.println("Exception from 'RemoveAthlete.java-updateTextArea': " + e.getMessage());
        }finally{
            try{
                if(stmt != null){
                    conn.close();
                }
            }catch(SQLException e){
                e.printStackTrace();
            }
        }
        
        String list = "";
        int i = 1;
        for(Athlete a : athleteList){
            list += i + ". " + a.getName() + "\n\tID: " + a.getID() + "\n\tAge: " + a.getAge() + "\n\tHeight: " + a.getHeight() + "\n\tWeight: " + a.getWeight() + "\n\n";
            i++;
        }
        
        athletesTextArea.setText(list);
    }
    
