    private void idTextFieldKeyReleased(java.awt.event.KeyEvent evt) {//GEN-FIRST:event_idTextFieldKeyReleased
        if(evt.getKeyCode() == KeyEvent.VK_ENTER){
            try{
                int data = Integer.parseInt(idTextField.getText());
                if(data < 0){
                    JOptionPane.showMessageDialog(this, "Please use positive numbers for the ID number", "Error", JOptionPane.ERROR_MESSAGE);
                    idTextField.setText("");
                }else{
                    Connection conn = null;
                    Statement stmt = null;
                    try{
                        Class.forName("org.sqlite.JDBC");
                        conn = DriverManager.getConnection(url);
                        stmt = conn.createStatement();

                        String exe = "SELECT sportID FROM Sport";
                        ResultSet rs = stmt.executeQuery(exe);
                        
                        Boolean found = false;
                        while(rs.next()){
                            if(rs.getInt("sportID") == data){
                                found = true;
                                break;
                            }
                        }
                        if(found){
                            JOptionPane.showMessageDialog(this, "ID number is already taken.", "Error", JOptionPane.ERROR_MESSAGE);
                            idTextField.setText("");
                        }else{
                            idNum = data;
                            idStatus = true;
                            idTextField.setFocusable(false);
                        }
                    }catch(SQLException e){
                        System.out.println("SQLException from 'AddSport-idTextField': " + e.getMessage());
                    }catch(Exception e){
                        System.out.println("Exception from 'AddSport-idTextField': " + e.getMessage());
                    }finally{
                        try{
                            if(stmt != null){
                                conn.close();
                            }
                        }catch(SQLException e){
                            e.printStackTrace();
                        }
                    }
                }
            }catch(NumberFormatException e){
                JOptionPane.showMessageDialog(this, "Input must be numeric", "Error", JOptionPane.ERROR_MESSAGE);
                idTextField.setText("");
            }
        }
    }//GEN-LAST:event_idTextFieldKeyReleased

