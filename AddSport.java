    private void insertSportButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_insertSportButtonActionPerformed
        if(idStatus && nameStatus && typeStatus){
            Connection conn = null;
            Statement stmt = null;
            try{
                Class.forName("org.sqlite.JDBC");
                conn = DriverManager.getConnection(url);
                stmt = conn.createStatement();
                
                String exe = "INSERT INTO Sport\nVALUES (" + idNum + ", '" + name + "', " + type + ")";
                stmt.executeUpdate(exe);
                System.out.println("Sport added!");
            }catch(SQLException e){
                System.out.println("SQLException from 'AddSport.java-insertSport': " + e.getMessage());
            }catch(Exception e){
                System.out.println("Exception from 'AddSport.java-insertSport': " + e.getMessage());
            }finally{
                try{
                    if(stmt != null){
                        conn.close();
                    }
                }catch(SQLException e){
                    e.printStackTrace();
                }
            }
            AddMenu addMenu = new AddMenu();
            this.setVisible(false);
            addMenu.setVisible(true);
        }else{
            JOptionPane.showMessageDialog(this, "Please enter required values.", "Error", JOptionPane.ERROR_MESSAGE);
        }
    }//GEN-LAST:event_insertSportButtonActionPerformed

